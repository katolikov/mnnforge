def emit_pattern(mnn_root: str, p: Pattern, log: Logger) -> Emission:
    """Emit .cl + Execution {hpp,cpp} for one pattern."""
    fp = p.fingerprint
    fp_camel = _fp_camel(fp)
    kernel_name = f"mnnforge_{fp}"
    n_inputs = len(p.occurrences[0].boundary_inputs)
    cl_source = synthesize_kernel(kernel_name, p.op_kinds, n_inputs)

    cl_path = os.path.join(_cl_dir(mnn_root), f"{kernel_name}.cl")
    hpp_path = os.path.join(_image_dir(mnn_root),
                            f"MnnForge{fp_camel}Execution.hpp")
    cpp_path = os.path.join(_image_dir(mnn_root),
                            f"MnnForge{fp_camel}Execution.cpp")

    with open(cl_path, "w") as fh:
        fh.write(cl_source)
    with open(hpp_path, "w") as fh:
        fh.write(EXEC_HPP_TPL.format(fp_camel=fp_camel))
    with open(cpp_path, "w") as fh:
        fh.write(EXEC_CPP_TPL.format(fp_camel=fp_camel,
                                     kernel_name=kernel_name))

    log.vinfo(f"  emitted {os.path.relpath(cl_path, mnn_root)}")
    log.vinfo(f"  emitted {os.path.relpath(hpp_path, mnn_root)}")
    log.vinfo(f"  emitted {os.path.relpath(cpp_path, mnn_root)}")

    return Emission(
        fingerprint=fp, fp_camel=fp_camel, kernel_name=kernel_name,
        n_inputs=n_inputs, cl_source=cl_source,
        cl_path=cl_path, exec_hpp_path=hpp_path, exec_cpp_path=cpp_path,
    )


# ---------------------------------------------------------------------------
# source/backend/cpu/MnnForgeCPU.cpp — universal CPU fallback
#
# Aligns with MNN's add-new-op SKILL step 3 ("CPU is the universal fallback").
# Without this:
#   * MNN's GeometryComputerUtils marks ops with all-const inputs as
#     Schedule::CONSTANT and tries to evaluate them on the backup CPU
#     backend, which has no OpType_Extra creator → "Don't support type
#     [Extra]" + "Const Folder Error" warnings.
#   * Models can't run at all on backends other than OpenCL.
#
# The CPU executor parses extra->attr["op_kinds"] (semicolon-separated
# "Type:sub_kind:chain_pos" tokens, see onnx_surgery.py) and walks the chain
# step-by-step in plain C++ on fp32 arrays. Slow but correct, and produces
# bit-exact same output as the OpenCL fast path on fp32 inputs.
#
# Registers a single creator for OpType_Extra that returns nullptr for
# non-MnnForge Extra ops, preserving the existing behaviour for everything
# else.
# ---------------------------------------------------------------------------

CPU_STUB_TPL = '''\
//
//  MnnForgeCPU.cpp
//  MNN
//
//  Auto-generated by mnnforge — do not edit by hand.
//
//  CPU fallback for OpType_Extra ops with type starting with "MnnForge_".
//  Walks the chain described in extra->attr["op_kinds"] and applies each
//  primitive on fp32 arrays in plain C++.
//
#include <math.h>
#include <string>
#include <vector>
#include <algorithm>
#include "core/Macro.h"
#include "core/TensorUtils.hpp"
#include "backend/cpu/CPUBackend.hpp"
#include "MNN_generated.h"

namespace MNN {

class MnnForgeCPUExecution : public Execution {
public:
    MnnForgeCPUExecution(Backend *backend, const Op *op,
                         const std::vector<std::pair<int, int>> &steps)
        : Execution(backend), mSteps(steps) {}
    virtual ~MnnForgeCPUExecution() = default;

    virtual ErrorCode onResize(const std::vector<Tensor *> &,
                               const std::vector<Tensor *> &) override {
        return NO_ERROR;
    }

    virtual ErrorCode onExecute(const std::vector<Tensor *> &inputs,
                                const std::vector<Tensor *> &outputs) override {
        if (inputs.empty() || outputs.empty()) return INVALID_VALUE;
        const int N = inputs[0]->elementSize();
        for (auto in : inputs) {
            if (in->elementSize() != N) return NOT_SUPPORT;
        }
        if (outputs[0]->elementSize() != N) return NOT_SUPPORT;
        if (inputs[0]->getType().bytes() != 4) return NOT_SUPPORT;

        std::vector<float> cur(N), tmp(N);
        size_t input_cursor = 0;
        auto readF = [](const Tensor *t, std::vector<float> &dst) {
            const float *src = t->host<float>();
            std::copy(src, src + dst.size(), dst.begin());
        };
        if (mSteps.empty()) return NOT_SUPPORT;

        for (size_t i = 0; i < mSteps.size(); ++i) {
            int kind = mSteps[i].first;
            int sub  = mSteps[i].second;
            if (i == 0) {
                if (input_cursor >= inputs.size()) return NOT_SUPPORT;
                readF(inputs[input_cursor++], cur);
            }
            if (kind == 0) {
                if (input_cursor >= inputs.size()) return NOT_SUPPORT;
                readF(inputs[input_cursor++], tmp);
                apply_binary(sub, cur, tmp);
            } else if (kind == 1) {
                apply_unary(sub, cur);
            } else if (kind == 2) {
                apply_relu(cur);
            }
        }

        float *dst = outputs[0]->host<float>();
        std::copy(cur.begin(), cur.end(), dst);
        return NO_ERROR;
    }

private:
    void apply_binary(int sub, std::vector<float> &a, const std::vector<float> &b) {
        const size_t N = a.size();
        for (size_t i = 0; i < N; ++i) {
            switch (sub) {
                case 0: a[i] = a[i] + b[i]; break;
                case 1: a[i] = a[i] - b[i]; break;
                case 2: a[i] = a[i] * b[i]; break;
                case 3: a[i] = a[i] / b[i]; break;
                default: break;
            }
        }
    }
    void apply_unary(int sub, std::vector<float> &a) {
        const size_t N = a.size();
        for (size_t i = 0; i < N; ++i) {
            float x = a[i];
            switch (sub) {
                case 0:  a[i] = std::fabs(x); break;
                case 1:  a[i] = -x; break;
                case 5:  a[i] = std::sqrt(x); break;
                case 6:  a[i] = 1.0f / std::sqrt(x); break;
                case 7:  a[i] = std::exp(x); break;
                case 8:  a[i] = std::log(x); break;
                case 15: a[i] = 1.0f / x; break;
                case 18: a[i] = std::tanh(x); break;
                case 23: a[i] = 1.0f / (1.0f + std::exp(-x)); break;
                case 31: a[i] = 0.5f * x * (1.0f + std::tanh(
                            0.79788456f * (x + 0.044715f * x * x * x))); break;
                default: break;
            }
        }
    }
    void apply_relu(std::vector<float> &a) {
        for (auto &v : a) v = v > 0 ? v : 0;
    }

    std::vector<std::pair<int, int>> mSteps;
};

static std::vector<std::pair<int, int>> _parse_op_kinds(const std::string &s) {
    std::vector<std::pair<int, int>> out;
    size_t pos = 0;
    while (pos < s.size()) {
        size_t end = s.find(';', pos);
        std::string tok = s.substr(pos, end == std::string::npos
                                       ? std::string::npos : end - pos);
        size_t c1 = tok.find(':');
        size_t c2 = tok.find(':', c1 == std::string::npos ? 0 : c1 + 1);
        if (c1 == std::string::npos || c2 == std::string::npos) break;
        std::string typeName = tok.substr(0, c1);
        int sub = std::atoi(tok.substr(c1 + 1, c2 - c1 - 1).c_str());
        int kind = -1;
        if      (typeName == "BinaryOp") kind = 0;
        else if (typeName == "UnaryOp")  kind = 1;
        else if (typeName == "ReLU")     kind = 2;
        if (kind >= 0) out.emplace_back(kind, sub);
        if (end == std::string::npos) break;
        pos = end + 1;
    }
    return out;
}

class MnnForgeCPUCreator : public CPUBackend::Creator {
public:
    virtual Execution *onCreate(const std::vector<Tensor *> &,
                                const std::vector<Tensor *> &,
                                const MNN::Op *op,
                                Backend *backend) const override {
        if (op->type() != OpType_Extra) return nullptr;
        auto extra = op->main_as_Extra();
        if (!extra || !extra->type()) return nullptr;
        std::string type_str = extra->type()->str();
        if (type_str.rfind("MnnForge_", 0) != 0) return nullptr;
        std::string kinds;
        if (extra->attr() != nullptr) {
            for (int i = 0; i < (int)extra->attr()->size(); ++i) {
                auto a = extra->attr()->Get(i);
                if (a && a->key() && a->key()->str() == "op_kinds" && a->s()) {
                    kinds = a->s()->str();
                    break;
                }
            }
        }
        if (kinds.empty()) return nullptr;
        auto steps = _parse_op_kinds(kinds);
        if (steps.empty()) return nullptr;
        return new MnnForgeCPUExecution(backend, op, steps);
    }
};

REGISTER_CPU_OP_CREATOR(MnnForgeCPUCreator, OpType_Extra);

}  // namespace MNN
'''


