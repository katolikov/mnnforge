# ---------------------------------------------------------------------------
# tools/converter/source/onnx/MnnForgeOnnx.cpp
#
# Registers a single converter class that handles every MnnForge_<fp> op type
# emitted by Phase 5 of mnnforge. The converter sets:
#     dstOp->type             = OpType_Extra
#     dstOp->main.value->type = onnxNode->op_type()    (e.g. "MnnForge_a3b8c12d")
#     dstOp->main.value->engine = "MNN"
# The "MNN" engine string is what writeFb.cpp checks before deciding whether
# the op is recognized — see source/common/writeFb.cpp:200. Without this, a
# user would have to pass `--allowCustomOp` to MNNConvert.
# ---------------------------------------------------------------------------

ONNX_CONVERTER_TPL = """\
//
//  MnnForgeOnnx.cpp
//  MNNConverter
//
//  Auto-generated by mnnforge — do not edit by hand.
//
//  Registers an ONNX→MNN converter for every MnnForge_<fp> op type emitted
//  by mnnforge. We set extra->engine = "MNN" so writeFb.cpp does not reject
//  the op (see source/common/writeFb.cpp).
//
#include "onnxOpConverter.hpp"

DECLARE_OP_CONVERTER(MnnForgeOnnx);

MNN::OpType MnnForgeOnnx::opType() {{
    return MNN::OpType_Extra;
}}
MNN::OpParameter MnnForgeOnnx::type() {{
    return MNN::OpParameter_Extra;
}}

void MnnForgeOnnx::run(MNN::OpT *dstOp, const onnx::NodeProto *onnxNode,
                       OnnxScope *scope) {{
    auto extra      = new MNN::ExtraT;
    dstOp->main.type  = MNN::OpParameter_Extra;
    dstOp->main.value = extra;
    extra->engine   = "MNN";   // accepted by writeFb.cpp
    extra->type     = onnxNode->op_type();   // e.g. "MnnForge_a3b8c12d"

    for (const auto &srcAttr : onnxNode->attribute()) {{
        std::unique_ptr<MNN::AttributeT> a(new MNN::AttributeT);
        a->key = srcAttr.name();
        switch (srcAttr.type()) {{
            case onnx::AttributeProto_AttributeType_INT:
                a->i = static_cast<int32_t>(srcAttr.i());
                break;
            case onnx::AttributeProto_AttributeType_FLOAT:
                a->f = srcAttr.f();
                break;
            case onnx::AttributeProto_AttributeType_STRING:
                a->s = srcAttr.s();
                break;
            case onnx::AttributeProto_AttributeType_INTS:
                a->list.reset(new MNN::ListValueT);
                a->list->i.resize(srcAttr.ints_size());
                for (int i = 0; i < srcAttr.ints_size(); ++i) {{
                    a->list->i[i] = static_cast<int32_t>(srcAttr.ints(i));
                }}
                break;
            case onnx::AttributeProto_AttributeType_FLOATS:
                a->list.reset(new MNN::ListValueT);
                a->list->f.resize(srcAttr.floats_size());
                for (int i = 0; i < srcAttr.floats_size(); ++i) {{
                    a->list->f[i] = srcAttr.floats(i);
                }}
                break;
            case onnx::AttributeProto_AttributeType_STRINGS:
                a->list.reset(new MNN::ListValueT);
                a->list->s.resize(srcAttr.strings_size());
                for (int i = 0; i < srcAttr.strings_size(); ++i) {{
                    a->list->s[i] = srcAttr.strings(i);
                }}
                break;
            default:
                break;
        }}
        extra->attr.emplace_back(std::move(a));
    }}
}}

// One REGISTER_CONVERTER line per fingerprint mined by mnnforge.
{registrations}
"""


def emit_onnx_converter(mnn_root: str, emissions: List[Emission],
                        log: Logger) -> str:
    """Emit (or refresh) tools/converter/source/onnx/MnnForgeOnnx.cpp.

    Re-running with a different set of fingerprints overwrites the file
    cleanly — registrations are auto-generated from the emission list.
    """
    out_dir = _onnx_converter_dir(mnn_root)
    if not os.path.isdir(out_dir):
        log.warn(f"converter dir missing: {out_dir} — skipping converter emit")
        return ""
    # One REGISTER_CONVERTER line per fingerprint.
    regs = "\n".join(
        f"REGISTER_CONVERTER(MnnForgeOnnx, MnnForge_{e.fingerprint});"
        for e in emissions
    )
    src = ONNX_CONVERTER_TPL.format(registrations=regs)
    fpath = os.path.join(out_dir, "MnnForgeOnnx.cpp")
    with open(fpath, "w") as fh:
        fh.write(src)
    log.ok(f"wrote {os.path.relpath(fpath, mnn_root)} "
           f"({len(emissions)} registration(s))")
    return fpath


