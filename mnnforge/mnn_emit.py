def _onnx_converter_dir(mnn_root: str) -> str:
    return os.path.join(mnn_root, "tools", "converter", "source", "onnx")


def _fp_camel(fp: str) -> str:
    # Hex string starts with a digit ~50% of the time. Prefix with 'F' if so.
    if not fp:
        return fp
    if fp[0].isdigit():
        return "F" + fp
    return fp[0].upper() + fp[1:]


