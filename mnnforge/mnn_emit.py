def regenerate_opencl_source_map(mnn_root: str, log: Logger) -> None:
    cl_dir = _cl_dir(mnn_root)
    script = os.path.join(cl_dir, "opencl_codegen.py")
    if not os.path.isfile(script):
        log.warn(f"opencl_codegen.py not found at {script}; "
                 "kernel string registry will be stale until you run it manually")
        return
    log.info("running opencl_codegen.py to refresh opencl_source_map.hpp")
    res = subprocess.run(
        ["python3", os.path.basename(script), "."],
        cwd=cl_dir, capture_output=True, text=True,
    )
    if res.returncode != 0:
        log.err(res.stdout)
        log.err(res.stderr)
        raise SystemExit(f"opencl_codegen.py failed (rc={res.returncode})")


