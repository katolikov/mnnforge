def _build_dispatch_block(emissions: List[Emission]) -> str:
    """Generate the include + dispatch snippet inserted into FuseExecution.cpp."""
    includes = "\n".join(
        f'#include "backend/opencl/execution/image/MnnForge{e.fp_camel}Execution.hpp"'
        for e in emissions
    )
    cases = "\n".join(
        f'    if (param->type()->str() == "MnnForge_{e.fingerprint}") '
        f'{{ return new MnnForge{e.fp_camel}Execution(inputs, backend, op); }}'
        for e in emissions
    )
    return (
        f"{MARKER_BEGIN}\n"
        f"{includes}\n"
        f"namespace MNN {{ namespace OpenCL {{\n"
        f"static Execution *_mnnforge_dispatch(\n"
        f"    const std::vector<Tensor *> &inputs,\n"
        f"    const MNN::Op *op, Backend *backend) {{\n"
        f"    auto param = op->main_as_Extra();\n"
        f"    if (!param || !param->type()) return nullptr;\n"
        f"{cases}\n"
        f"    return nullptr;\n"
        f"}}\n"
        f"}} }}\n"
        f"{MARKER_END}\n"
    )


def patch_fuse_execution(mnn_root: str, emissions: List[Emission],
                         log: Logger) -> None:
    """Insert/refresh the mnnforge dispatch in FuseExecution.cpp.

    Idempotent: a previous MNNFORGE-BEGIN/END block is replaced atomically.
    Adds one dispatcher invocation at the start of FuseCreator::onCreate.
    """
    fpath = os.path.join(_image_dir(mnn_root), "FuseExecution.cpp")
    if not os.path.isfile(fpath):
        raise SystemExit(f"FuseExecution.cpp missing: {fpath}")

    # Backup once on first patch.
    backup = fpath + ".mnnforge.bak"
    if not os.path.exists(backup):
        shutil.copy2(fpath, backup)
        log.vinfo(f"  backup created: {backup}")

    src = open(fpath).read()

    # Strip any previous MNNFORGE block.
    src = re.sub(
        re.escape(MARKER_BEGIN) + r".*?" + re.escape(MARKER_END) + r"\n?",
        "", src, flags=re.DOTALL,
    )
    # Strip our previously injected dispatch call inside FuseCreator (if any).
    src = re.sub(
        r"\s*//\s*MNNFORGE-DISPATCH\b.*?//\s*MNNFORGE-DISPATCH-END\n?",
        "\n", src, flags=re.DOTALL,
    )

    block = _build_dispatch_block(emissions)
    # Insert the block just before the namespace MNN line.
    ns_match = re.search(r"^namespace MNN\b", src, flags=re.MULTILINE)
    if ns_match is None:
        raise SystemExit("could not locate `namespace MNN` in FuseExecution.cpp")
    insert_at = ns_match.start()
    src = src[:insert_at] + block + "\n" + src[insert_at:]

    # Insert the dispatcher invocation at the top of FuseCreator::onCreate body.
    onCreate_re = re.compile(
        r"(class FuseCreator\b[^{]*\{[^}]*?onCreate\([^{]*\{)",
        re.DOTALL,
    )
    m = onCreate_re.search(src)
    if m is None:
        log.warn(
            "couldn't find FuseCreator::onCreate body — dispatch block "
            "was inserted but won't be called. Inspect FuseExecution.cpp."
        )
    else:
        injection = (
            "\n        // MNNFORGE-DISPATCH\n"
            "        if (auto *e = MNN::OpenCL::_mnnforge_dispatch(inputs, op, backend)) "
            "return e;\n"
            "        // MNNFORGE-DISPATCH-END"
        )
        src = src[:m.end()] + injection + src[m.end():]

    with open(fpath, "w") as fh:
        fh.write(src)
    log.ok(f"patched {os.path.relpath(fpath, mnn_root)} "
           f"(dispatch for {len(emissions)} kernel(s))")


def emit_all(mnn_root: str, patterns: List[Pattern], top_n: int,
             log: Logger) -> List[Emission]:
    accepted = patterns[:top_n]
    if not accepted:
        log.info("no patterns to emit")
        return []
    emissions: List[Emission] = []
    for p in accepted:
        try:
            e = emit_pattern(mnn_root, p, log)
            emissions.append(e)
        except ValueError as e:
            log.warn(f"  pattern {p.fingerprint}: synthesis failed ({e}) — skipped")
    if not emissions:
        return []
    regenerate_opencl_source_map(mnn_root, log)
    patch_fuse_execution(mnn_root, emissions, log)
    emit_onnx_converter(mnn_root, emissions, log)
    emit_cpu_stub(mnn_root, log)
    log.ok(f"emitted {len(emissions)} kernel/Execution pair(s) into MNN tree")
    return emissions


# ---------------------------------------------------------------------------
# Rollback (so users can revert mnnforge's MNN-tree edits)
# ---------------------------------------------------------------------------

def rollback(mnn_root: str, log: Logger) -> int:
    """Restore FuseExecution.cpp from .bak and remove all generated files."""
    fpath = os.path.join(_image_dir(mnn_root), "FuseExecution.cpp")
    backup = fpath + ".mnnforge.bak"
    n_removed = 0
    if os.path.exists(backup):
        shutil.copy2(backup, fpath)
        os.remove(backup)
        log.ok(f"restored {os.path.relpath(fpath, mnn_root)}")
    # Remove generated files in source/backend/opencl/execution/cl
    for fn in os.listdir(_cl_dir(mnn_root)):
        if fn.startswith("mnnforge_") and (fn.endswith(".cl")
                                           or fn.endswith("_mnn_cl.cpp")):
            os.remove(os.path.join(_cl_dir(mnn_root), fn))
            n_removed += 1
    # Remove generated files in source/backend/opencl/execution/image
    for fn in os.listdir(_image_dir(mnn_root)):
        if fn.startswith("MnnForge") and (fn.endswith(".hpp")
                                          or fn.endswith(".cpp")):
            os.remove(os.path.join(_image_dir(mnn_root), fn))
            n_removed += 1
    # Remove generated converter in tools/converter/source/onnx
    onnx_dir = _onnx_converter_dir(mnn_root)
    if os.path.isdir(onnx_dir):
        cand = os.path.join(onnx_dir, "MnnForgeOnnx.cpp")
        if os.path.exists(cand):
            os.remove(cand)
            n_removed += 1
    # Remove CPU stub in source/backend/cpu
    cpu_dir = _cpu_dir(mnn_root)
    if os.path.isdir(cpu_dir):
        cand = os.path.join(cpu_dir, "MnnForgeCPU.cpp")
        if os.path.exists(cand):
            os.remove(cand)
            n_removed += 1
    log.ok(f"removed {n_removed} generated file(s)")
    # Re-run codegen so opencl_source_map.hpp drops the stale entries.
    regenerate_opencl_source_map(mnn_root, log)
    return n_removed
