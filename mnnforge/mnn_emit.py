EXEC_HPP_TPL = """\
//
//  MnnForge{fp_camel}Execution.hpp
//  MNN
//
//  Auto-generated by mnnforge — do not edit by hand.
//
#ifndef MnnForge{fp_camel}Execution_hpp
#define MnnForge{fp_camel}Execution_hpp

#include "backend/opencl/execution/image/CommonExecution.hpp"

namespace MNN {{
namespace OpenCL {{

class MnnForge{fp_camel}Execution : public CommonExecution {{
public:
    MnnForge{fp_camel}Execution(const std::vector<Tensor *> &inputs, Backend *backend, const Op *op);
    virtual ~MnnForge{fp_camel}Execution() = default;
    virtual ErrorCode onEncode(const std::vector<Tensor *> &inputs,
                               const std::vector<Tensor *> &outputs) override;

private:
    OpenCLBackend *mOpenCLBackend = nullptr;
    uint32_t mMaxWorkGroupSize = 0;
    std::vector<uint32_t> mGlobalWorkSize{{1, 1, 1}};
    std::vector<uint32_t> mLocalWorkSize{{1, 1, 1}};
}};

}}  // namespace OpenCL
}}  // namespace MNN

#endif
"""


EXEC_CPP_TPL = """\
//
//  MnnForge{fp_camel}Execution.cpp
//  MNN
//
//  Auto-generated by mnnforge — do not edit by hand.
//
#include "backend/opencl/execution/image/MnnForge{fp_camel}Execution.hpp"
#include "backend/opencl/core/OpenCLBackend.hpp"
#include "backend/opencl/core/OpenCLRunningUtils.hpp"
#include "core/Macro.h"

namespace MNN {{
namespace OpenCL {{

MnnForge{fp_camel}Execution::MnnForge{fp_camel}Execution(
        const std::vector<Tensor *> &inputs, Backend *backend, const Op *op)
    : CommonExecution(backend, op) {{
    mUnits.resize(1);
    mOpenCLBackend = static_cast<OpenCLBackend *>(backend);
    auto runtime = mOpenCLBackend->getOpenCLRuntime();
    std::set<std::string> buildOptions;
    // Kernel program is registered in opencl_source_map.hpp as "{kernel_name}".
    mUnits[0].kernel = runtime->buildKernel(
        "{kernel_name}", "{kernel_name}", buildOptions,
        mOpenCLBackend->getPrecision(), inputs[0], inputs[0]);
    OPENCL_CHECK_KERNEL_CTOR(mUnits[0].kernel);
    mMaxWorkGroupSize = static_cast<uint32_t>(
        runtime->getMaxWorkGroupSize(mUnits[0].kernel));
}}

ErrorCode MnnForge{fp_camel}Execution::onEncode(
        const std::vector<Tensor *> &inputs,
        const std::vector<Tensor *> &outputs) {{
    auto &unit = mUnits[0];
    Tensor *output = outputs[0];
    std::vector<int> outputShape = tensorShapeFormat(output);
    const int outputBatch    = outputShape.at(0);
    const int outputHeight   = outputShape.at(1);
    const int outputWidth    = outputShape.at(2);
    const int outputChannels = outputShape.at(3);
    const int channelBlocks  = UP_DIV(outputChannels, 4);

    mGlobalWorkSize = {{
        static_cast<uint32_t>(channelBlocks),
        static_cast<uint32_t>(outputWidth),
        static_cast<uint32_t>(outputHeight * outputBatch),
    }};

    cl_int ret = CL_SUCCESS;
    uint32_t idx = 0;
    for (auto in : inputs) {{
        ret |= unit.kernel->get().setArg(idx++, openCLImage(in));
    }}
    for (auto out : outputs) {{
        ret |= unit.kernel->get().setArg(idx++, openCLImage(out));
    }}
    ret |= unit.kernel->get().setArg(idx++, mGlobalWorkSize[0]);
    ret |= unit.kernel->get().setArg(idx++, mGlobalWorkSize[1]);
    ret |= unit.kernel->get().setArg(idx++, mGlobalWorkSize[2]);
    MNN_CHECK_CL_SUCCESS(ret, "setArg MnnForge{fp_camel}");

    mLocalWorkSize = localWS3DDefault(
        mGlobalWorkSize, mMaxWorkGroupSize,
        mOpenCLBackend->getOpenCLRuntime(),
        "{kernel_name}", unit.kernel,
        mOpenCLBackend->getCLTuneLevel(), "MnnForge").first;
    mOpenCLBackend->recordKernel3d(unit.kernel, mGlobalWorkSize, mLocalWorkSize);
    unit.globalWorkSize = {{mGlobalWorkSize[0], mGlobalWorkSize[1], mGlobalWorkSize[2]}};
    unit.localWorkSize  = {{mLocalWorkSize[0], mLocalWorkSize[1], mLocalWorkSize[2]}};
    return NO_ERROR;
}}

}}  // namespace OpenCL
}}  // namespace MNN
"""


